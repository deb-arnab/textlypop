    <h2>What TextlyPop does</h2>
    <p>TextlyPop gives you instant access to tools that solve real everyday problems with text. Analyze your writing with the word counter, character counter, sentence counter, reading level checker and word frequency counter. Convert text between formats with the case converter, text to slug, comma separator, line break to comma and number to words tools. Clean up messy text with the remove line breaks, remove extra spaces, duplicate line remover, find and replace and text line sorter tools. Generate content with the lorem ipsum generator, password generator and random number generator. Work with code using the JSON formatter, HTML encoder decoder, URL encoder decoder, Markdown to HTML, HTML to Markdown and base converter. Use speech tools to convert text to speech or speech to text directly in your browser. Every tool produces results in real time as you type with no submit buttons and no waiting.</p>

    <h2>Why TextlyPop exists</h2>
    <p>Most text tool websites are slow, cluttered with ads that obscure the actual tool, and require unnecessary account creation. TextlyPop was built around three principles. </p>
    <p>Speed — results appear in under 50 milliseconds as you type because everything processes in your browser with no network round-trip. </p>
    <p>Privacy — your text never leaves your device because all computation happens in JavaScript locally. </p>
    <p>Simplicity — every tool does one thing and does it well with a clean interface that works on any device from a phone to a wide desktop monitor.</p>

    <h2>Your privacy</h2>
    <p>TextlyPop does not collect your text, does not set tracking cookies, and does not build profiles of your usage. All tool processing happens in your browser. The only data stored locally is your theme preference, your recently used tools list, and auto-saved tool input — all in your browser's own localStorage, never on TextlyPop servers. You can clear it at any time through your browser settings. TextlyPop displays ads through Google AdSense to support the free service. No user text content is ever collected or used for advertising.</p>

